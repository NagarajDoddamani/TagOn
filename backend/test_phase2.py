def t(name, method, path, exp, auth=None, json_data=None):
    global passed, failed
    h = {"Authorization": f"Bearer {auth}"} if auth else {}
    try:
        if method == "GET":
            r = requests.get(BASE + path, headers=h, timeout=10)
        else:
            r = requests.post(BASE + path, headers=h, json=json_data, timeout=10)
        ok = r.status_code == exp
        if ok:
            passed += 1
        else:
            failed += 1
        status = "PASS" if ok else "FAIL"
        detail = ""
        try:
            body = r.json()
            if isinstance(body, dict) and "detail" in body:
                detail = f" - {body['detail']}"
        except:
            detail = f" - {r.text[:100]}"
        print(f"  [{status}] {name} -> {r.status_code}{detail}")
    except Exception as e:
        failed += 1
        print(f"  [FAIL] {name} -> ERROR: {e}")

